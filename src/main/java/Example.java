    /**
     * Creates a LaTeX table of results from Alloy outputs.
     */
    public static String createTable() throws IOException {
        Map<String, Map<String, Map<String, Integer>>> tables = new HashMap<>();
        tables.put("English2Alloy", new HashMap<>());
        tables.put("Alloy2Alloy", new HashMap<>());

        Pattern totalP = Pattern.compile("Total responses:\\s*(\\d+)");
        Pattern uniqueP = Pattern.compile("Unique responses:\\s*(\\d+)");
        Pattern correctP = Pattern.compile("CORRECT_CODE responses:\\s*(\\d+)");
        Pattern sameP = Pattern.compile("SAME_CODE responses:\\s*(\\d+)");
        Pattern syntaxP = Pattern.compile("SYNTAX_ERROR responses:\\s*(\\d+)");
        Pattern diffP = Pattern.compile("DIFF_OUTPUT responses:\\s*(\\d+)");

        Path resultDir = Paths.get("result");
        if (!Files.exists(resultDir) || !Files.isDirectory(resultDir)) {
            throw new IOException("result directory does not exist");
        }
        try (DirectoryStream<Path> stream = Files.newDirectoryStream(resultDir, "*.txt")) {
            for (Path entry : stream) {
                String fileName = entry.getFileName().toString();
                String[] parts = fileName.split("_");
                if (parts.length < 4) continue;
                String type = parts[2];
                String predicate = parts[3];
                String content = new String(Files.readAllBytes(entry), StandardCharsets.UTF_8);
                Matcher mTotal = totalP.matcher(content);
                Matcher mUnique = uniqueP.matcher(content);
                Matcher mCorrect = correctP.matcher(content);
                Matcher mSame = sameP.matcher(content);
                Matcher mSyntax = syntaxP.matcher(content);
                Matcher mDiff = diffP.matcher(content);
                int total = mTotal.find() ? Integer.parseInt(mTotal.group(1)) : 0;
                int unique = mUnique.find() ? Integer.parseInt(mUnique.group(1)) : 0;
                int correct = mCorrect.find() ? Integer.parseInt(mCorrect.group(1)) : 0;
                int same = mSame.find() ? Integer.parseInt(mSame.group(1)) : 0;
                int syntaxError = mSyntax.find() ? Integer.parseInt(mSyntax.group(1)) : 0;
                int diffOutput = mDiff.find() ? Integer.parseInt(mDiff.group(1)) : 0;

                Map<String, Integer> stats = new HashMap<>();
                stats.put("Total", total);
                stats.put("Unique", unique);
                stats.put("Correct", correct);
                stats.put("SAME_CODE", same);
                stats.put("SYNTAX_ERROR", syntaxError);
                stats.put("DIFF_OUTPUT", diffOutput);

                if (tables.containsKey(type)) {
                    tables.get(type).put(predicate, stats);
                }
            }
        }

        StringBuilder sb = new StringBuilder();
        for (String type : tables.keySet()) {
            sb.append("\\begin{table}\n")
                    .append("\\caption{Result ").append(type).append("}\\label{tab_").append(type).append("}\n")
                    .append("\\begin{tabular}{|l|l|l|l|l|}\n")
                    .append("\\hline\n")
                    .append("Predicate & Unique & Cor & Syn & Sem\\\\\n")
                    .append("\\hline\n");
            List<String> predicates = tables.get(type).keySet().stream().sorted().collect(Collectors.toList());
            for (String pred : predicates) {
                Map<String, Integer> stats = tables.get(type).get(pred);
                int combinedCorrect = stats.get("Correct") + stats.get("SAME_CODE");
                sb.append(pred).append(" & ")
                        .append(stats.get("Unique")).append(" & ")
                        .append(combinedCorrect).append(" & ")
                        .append(stats.get("SYNTAX_ERROR")).append(" & ")
                        .append(stats.get("DIFF_OUTPUT")).append(" \\\\\n")
                        .append("\\hline\n");
            }
            int currentRows = predicates.size();
            for (int i = currentRows; i < 11; i++) {
                sb.append(" &  &  &  &  \\\\\n\\hline\n");
            }
            sb.append("\\end{tabular}\n")
                    .append("\\end{table}\n\n");
        }
        return sb.toString();
    }

