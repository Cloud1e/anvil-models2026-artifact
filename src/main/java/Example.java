    /**
     * Processes Alloy input to determine its syntactical and logical validity,
     * and classifies the result based on predefined criteria.
     *
     * @param input   The Alloy model as a string.
     * @param verbose If true, prints detailed information about the processing to the console.
     * @return CodeType indicating the classification of the input
     */
    static CodeType AlloyProcess(String input, boolean verbose) {
        A4Reporter rep = new A4Reporter();
        A4Options opt = new A4Options();
        CodeType type = CodeType.CORRECT_CODE;


        try {
            CompModule world = CompUtil.parseEverything_fromString(rep, input);

            // When used via --check-one we may be given a "model-only" file (no run/check commands).
            // In that case, treat the module as syntactically valid but skip command execution instead
            // of throwing an IndexOutOfBounds on world.getAllCommands().get(0).
            if (world.getAllCommands().isEmpty()) {
                if (verbose) System.out.println("No commands in module; treating as DIFF_OUTPUT.");
                return CodeType.DIFF_OUTPUT;
            }

            PrintStream originalOut = System.out;
            String output = null;
            A4Solution solution = null;
            try {
                ByteArrayOutputStream outputStream = new ByteArrayOutputStream();
                PrintStream printStream = new PrintStream(outputStream);
                System.setOut(printStream);

                solution = TranslateAlloyToKodkod.execute_command(null, world.getAllReachableSigs(), world.getAllCommands().get(0), opt);
                output = outputStream.toString();

                if (!output.contains(symbol)) {
                    type = CodeType.SAME_CODE;
                }

                printStream.close();
                outputStream.close();
            } catch (IOException e) {
                if (verbose) e.printStackTrace();
            } finally {
                System.setOut(originalOut);
                if (verbose) System.out.println(output);
            }

            if (verbose) System.out.println("Solutions");
            if (verbose) System.out.println(solution.satisfiable());
            assert solution != null;
            if (solution.satisfiable()) {
                type = CodeType.DIFF_OUTPUT;
                if (verbose) System.out.println(solution);
                solution.next();
            }
        } catch (Exception e) {
            type = CodeType.SYNTAX_ERROR;
            lastSyntaxErrorMessage = e.getMessage() != null ? e.getMessage() : e.toString();
            if (verbose) e.printStackTrace();
        }

        if (verbose) System.out.println(type);
        return type;

    }

    /** Strip top-level {@code run}/{@code check} lines so the module can end with a single equivalence check. */
    private static String stripRunAndCheckLines(String text) {
        StringBuilder sb = new StringBuilder();
        for (String line : text.split("\\R")) {
            String t = line.trim();
            if (t.startsWith("run ") || t.startsWith("check ")) {
                continue;
            }
            sb.append(line).append("\n");
        }
        return sb.toString().trim();
    }

    /**
     * Boolean formula inside {@code check { ... }} for {@code predName <=> otherPredName}, same arity.
     * Matches ProcessNewResults / pilot Python: 0-ary, {@code (...)} params, or {@code [...]} relation style.
     */
    private static String equivalenceCheckInnerExpression(String predName, String headerLine, String otherPredName) {
        String hdr = headerLine.trim();
        if (!hdr.startsWith("pred")) {
            throw new IllegalArgumentException("expected pred header line");
        }
        String rest = hdr.substring(4).trim();
        if (!rest.startsWith(predName)) {
            throw new IllegalArgumentException("expected pred " + predName + " in header");
        }
        String tail = rest.substring(predName.length()).trim();
        if (tail.isEmpty() || tail.startsWith("{")) {
            return predName + " <=> " + otherPredName;
        }
        if (tail.startsWith("()")) {
            return predName + " <=> " + otherPredName;
        }
        if (tail.charAt(0) != '(' && tail.charAt(0) != '[') {
            return predName + " <=> " + otherPredName;
        }
        char openC = tail.charAt(0);
        char closeC = openC == '(' ? ')' : ']';
        int depth = 0;
        int startIdx = 0;
        String inner = "";
        for (int i = 0; i < tail.length(); i++) {
            char ch = tail.charAt(i);
            if (ch == openC) {
                depth++;
                if (depth == 1) {
                    startIdx = i + 1;
                }
            } else if (ch == closeC) {
                depth--;
                if (depth == 0) {
                    inner = tail.substring(startIdx, i).trim();
                    break;
                }
            }
        }
        if (inner.isEmpty()) {
            return predName + " <=> " + otherPredName;
        }
        if (openC == '(') {
            Pattern p = Pattern.compile("\\b(\\w+)\\s*:");
            Matcher m = p.matcher(inner);
            List<String> names = new ArrayList<>();
            while (m.find()) {
                names.add(m.group(1));
            }
            if (names.isEmpty()) {
                throw new IllegalArgumentException("could not parse param names from (" + inner + ")");
            }
            String inv = "[" + String.join(", ", names) + "]";
            return "all " + inner + " | " + predName + inv + " <=> " + otherPredName + inv;
        }
        if (inner.contains(":")) {
            int c = inner.lastIndexOf(':');
            String lhs = inner.substring(0, c);
            String[] parts = lhs.split(",");
            List<String> names = new ArrayList<>();
            for (String part : parts) {
                String[] tok = part.trim().split("\\s+");
                if (tok.length > 0) {
                    names.add(tok[0]);
                }
            }
            String inv = "[" + String.join(", ", names) + "]";
            return "all " + inner + " | " + predName + inv + " <=> " + otherPredName + inv;
        }
        String[] raw = inner.split(",");
        List<String> names2 = new ArrayList<>();
        for (String r : raw) {
            names2.add(r.trim());
        }
        String inv2 = "[" + String.join(", ", names2) + "]";
        return "all " + inner + " | " + predName + inv2 + " <=> " + otherPredName + inv2;
    }

    private static String predHeaderBeforeBrace(String fullPredDef) {
        int k = fullPredDef.indexOf('{');
        if (k < 0) {
            return fullPredDef.trim();
        }
        return fullPredDef.substring(0, k).trim();
    }

    /** First word-boundary occurrence of {@code predName} in header becomes {@code newName} (pilot script parity). */
    private static String renameFirstPredNameInHeader(String headerBlock, String predName, String newName) {
        Pattern pat = Pattern.compile("\\b" + Pattern.quote(predName) + "\\b");
        Matcher m = pat.matcher(headerBlock);
        if (!m.find()) {
            throw new IllegalArgumentException("pred " + predName + " not found in header: " + headerBlock);
        }
        return m.replaceFirst(Matcher.quoteReplacement(newName));
    }

    /**
     * One bounded {@code check { P <=> P__cand }} module: gold {@code pred} from {@code originalPredDefs},
     * candidate body from the response line, rest from {@code sigAndFactsStr} and {@code otherPredicates}.
     */
    private static String buildArepairEquivalenceModule(
            String sigAndFactsStr,
            Map<String, String> otherPredicates,
            String predName,
            String predBody,
            Map<String, String> originalPredDefs) {
        String goldDef = originalPredDefs.get(predName);
        if (goldDef == null) {
            return null;
        }
        String headerPart = predHeaderBeforeBrace(goldDef);
        String candHeader = renameFirstPredNameInHeader(headerPart, predName, predName + "__cand");
        String candBlock = candHeader + " {\n  " + predBody + "\n}\n";
        StringBuilder sb = new StringBuilder();
        sb.append(sigAndFactsStr).append("\n\n");
        for (Map.Entry<String, String> e : otherPredicates.entrySet()) {
            if (e.getKey().equals(predName)) {
                continue;
            }
            sb.append(e.getValue()).append("\n\n");
        }
        sb.append(goldDef.trim()).append("\n\n");
        sb.append(candBlock);
        String stripped = stripRunAndCheckLines(sb.toString());
        String inner = equivalenceCheckInnerExpression(predName, headerPart, predName + "__cand");
        int scope = 5;
        try {
            scope = Integer.parseInt(System.getProperty("arepair.equiv.scope", "5"));
        } catch (NumberFormatException ignored) {
            // keep default
        }
        return stripped + "\n\ncheck { " + inner + " } for " + scope;
    }

    /** Parsed base model for bounded {@code pred <=> pred__cand} (same shape as ProcessARepairResultsEquivalence). */
    private static class EquivParseContext {
        String sigAndFactsStr;
        final Map<String, String> otherPredicates = new HashMap<>();
        final Map<String, String> originalPredDefs = new HashMap<>();
    }

    /**
     * Parse a clean base model (e.g. {@code ARepair/experiments/models/M.als}) into sig/facts + predicate defs.
     */
    private static EquivParseContext parseBaseModelContentForEquiv(String originalModelContent) {
        EquivParseContext ctx = new EquivParseContext();
        String[] lines = originalModelContent.split("\n");
        StringBuilder sigAndFacts = new StringBuilder();
        for (int i = 0; i < lines.length; i++) {
            String line = lines[i];
            String trimmedLine = line.trim();
            if (trimmedLine.startsWith("pred ") && !trimmedLine.startsWith("pred RepOk")) {
                String predLine = line;
                int bracePos = trimmedLine.indexOf("{");
                if (bracePos == -1) {
                    StringBuilder sigBuilder = new StringBuilder();
                    for (int j = i; j < lines.length; j++) {
                        sigBuilder.append(lines[j]).append("\n");
                        if (lines[j].trim().contains("{")) {
                            break;
                        }
                    }
                    predLine = sigBuilder.toString().trim();
                }
                String pName = trimmedLine.substring(5).trim();
                int nameEnd = pName.length();
                for (char delimiter : new char[]{'[', '(', '{'}) {
                    int pos = pName.indexOf(delimiter);
                    if (pos > 0 && pos < nameEnd) {
                        nameEnd = pos;
                    }
                }
                pName = pName.substring(0, nameEnd).trim();

                StringBuilder fullPred = new StringBuilder();
                int braceCount = 0;
                boolean inPred = false;
                int endIndex = i;
                for (int j = i; j < lines.length; j++) {
                    fullPred.append(lines[j]).append("\n");
                    for (char c : lines[j].toCharArray()) {
                        if (c == '{') {
                            braceCount++;
                            inPred = true;
                        }
                        if (c == '}') {
                            braceCount--;
                        }
                    }
                    if (inPred && braceCount == 0) {
                        endIndex = j;
                        break;
                    }
                }
                String fullPredDefRaw = fullPred.toString();
                String fullPredDef = fullPredDefRaw.trim();
                ctx.originalPredDefs.put(pName, fullPredDefRaw);
                ctx.otherPredicates.put(pName, fullPredDef);
                i = endIndex;
            } else if (trimmedLine.startsWith("fun ")) {
                StringBuilder fullFun = new StringBuilder();
                int braceCount = 0;
                boolean inFun = false;
                int endIndex = i;
                for (int j = i; j < lines.length; j++) {
                    fullFun.append(lines[j]).append("\n");
                    for (char c : lines[j].toCharArray()) {
                        if (c == '{') {
                            braceCount++;
                            inFun = true;
                        }
                        if (c == '}') {
                            braceCount--;
                        }
                    }
                    if (inFun && braceCount == 0) {
                        endIndex = j;
                        break;
                    }
                }
                sigAndFacts.append(fullFun.toString());
                i = endIndex;
            } else if (trimmedLine.startsWith("run ") || trimmedLine.startsWith("check ") || trimmedLine.startsWith("assert ")) {
                break;
            } else {
                sigAndFacts.append(line).append("\n");
            }
        }
        ctx.sigAndFactsStr = sigAndFacts.toString().trim();
        return ctx;
    }

    /** Inner body of {@code pred predName} in {@code modelContent}, or null if not found. */
    private static String extractPredicateInnerBody(String modelContent, String predName) {
        String[] lines = modelContent.split("\n");
        for (int i = 0; i < lines.length; i++) {
            String line = lines[i];
            String trimmedLine = line.trim();
            if (!trimmedLine.startsWith("pred ") || trimmedLine.startsWith("pred RepOk")) {
                continue;
            }
            int bracePos = trimmedLine.indexOf("{");
            if (bracePos == -1) {
                StringBuilder sigBuilder = new StringBuilder();
                for (int j = i; j < lines.length; j++) {
                    sigBuilder.append(lines[j]).append("\n");
                    if (lines[j].trim().contains("{")) {
                        break;
                    }
                }
            }
            String pName = trimmedLine.substring(5).trim();
            int nameEnd = pName.length();
            for (char delimiter : new char[]{'[', '(', '{'}) {
                int pos = pName.indexOf(delimiter);
                if (pos > 0 && pos < nameEnd) {
                    nameEnd = pos;
                }
            }
            pName = pName.substring(0, nameEnd).trim();
            if (!predName.equals(pName)) {
                continue;
            }

            StringBuilder fullPred = new StringBuilder();
            int braceCount = 0;
            boolean inPred = false;
            for (int j = i; j < lines.length; j++) {
                fullPred.append(lines[j]).append("\n");
                for (char c : lines[j].toCharArray()) {
                    if (c == '{') {
                        braceCount++;
                        inPred = true;
                    }
                    if (c == '}') {
                        braceCount--;
                    }
                }
                if (inPred && braceCount == 0) {
                    break;
                }
            }
            String fullPredDefRaw = fullPred.toString();
            int bo = fullPredDefRaw.indexOf("{");
            int bc = fullPredDefRaw.lastIndexOf("}");
            if (bo < 0 || bc <= bo) {
                return null;
            }
            return fullPredDefRaw.substring(bo + 1, bc);
        }
        return null;
    }

    /**
     * Bounded {@code check { P <=> P__cand }}: gold predicate from base model, candidate body from faulty model.
     * Prints {@link CodeType} on first line (same convention as {@code --check-one}).
     */
    static void runFaultyPredEquivCheck(String basePath, String faultyPath, String predName) throws Exception {
        String baseContent = new String(Files.readAllBytes(Paths.get(basePath)), StandardCharsets.UTF_8);
        String faultyContent = new String(Files.readAllBytes(Paths.get(faultyPath)), StandardCharsets.UTF_8);
        EquivParseContext ctx = parseBaseModelContentForEquiv(baseContent);
        if (ctx.sigAndFactsStr == null || ctx.sigAndFactsStr.isEmpty()) {
            System.out.println(CodeType.SYNTAX_ERROR);
            System.out.println("Could not extract sig/facts from base model");
            return;
        }
        if (!ctx.originalPredDefs.containsKey(predName)) {
            System.out.println(CodeType.SYNTAX_ERROR);
            System.out.println("Predicate not found in base model: " + predName);
            return;
        }
        String faultyBody = extractPredicateInnerBody(faultyContent, predName);
        if (faultyBody == null) {
            System.out.println(CodeType.SYNTAX_ERROR);
            System.out.println("Predicate not found in faulty model: " + predName);
            return;
        }
        String equivModel;
        try {
            equivModel = buildArepairEquivalenceModule(
                    ctx.sigAndFactsStr, ctx.otherPredicates, predName, faultyBody.trim(), ctx.originalPredDefs);
        } catch (IllegalArgumentException ex) {
            System.out.println(CodeType.SYNTAX_ERROR);
            System.out.println(ex.getMessage());
            return;
        }
        if (equivModel == null) {
            System.out.println(CodeType.SYNTAX_ERROR);
            System.out.println("Equiv module build failed");
            return;
        }
        lastSyntaxErrorMessage = null;
        CodeType t = AlloyProcess(equivModel, false);
        System.out.println(t);
        if (t == CodeType.SYNTAX_ERROR && lastSyntaxErrorMessage != null) {
            System.out.println(lastSyntaxErrorMessage);
        }
    }


