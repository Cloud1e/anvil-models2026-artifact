    /**
     * Extracts content between specified markers from the given input, with optional prepending/appending of markers.
     *
     * @param input       The input string from which content is to be extracted.
     * @param startMarker The starting marker to look for.
     * @param endMarker   The ending marker to look for.
     * @return Extracted content with minimal whitespace and markers correctly placed. Returns an error message if markers are not found appropriately.
     */
    public static String extractContent(String input, String startMarker, String endMarker) {
        // Normalize line endings and remove single line comments
        input = input.replaceAll("//.*?\\n", "").replaceAll("\\r\\n?", "\n");

        // Remove leading spaces from each line and trim trailing spaces (Java 8: use split + trim instead of lines() + strip())
        String[] lineArr = input.split("\\r?\\n");
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < lineArr.length; i++) {
            if (i > 0) sb.append("\n");
            sb.append(lineArr[i].trim());
        }
        input = sb.toString();

        int startIndex = input.indexOf(startMarker);
        int endIndex = input.indexOf(endMarker, startIndex) + endMarker.length();
        int firstBracketsIndex = input.indexOf("{");

        // Check if the content is already appropriately wrapped
        if (input.trim().startsWith("{") && input.trim().endsWith("}")) {
            // Format it with the new startMarker and endMarker without duplicating braces
            String trimmedContent = input.trim().substring(1, input.trim().length() - 1).trim();
            return startMarker + "\n" + trimmedContent + "\n" + endMarker;
        }

        // If startMarker is not found, prepend it to the input
        String content;
        if (startIndex == -1) {
            if (firstBracketsIndex != -1) {
                content = startMarker + "\n" + input.substring(firstBracketsIndex + 1, endIndex).trim();
            } else
                content = startMarker + " " + input.trim();
        } else if (endIndex == -1 || endIndex <= startIndex) {
            // If endMarker is not found or occurs before startMarker, return an error message
            return "Appropriate markers not found.";
        } else {
            // Extract the content between startMarker and the first occurrence of endMarker after startMarker
            content = input.substring(startIndex, endIndex);
        }

        // If endMarker was initially missing, append it
        if (!content.endsWith(endMarker)) {
            content += " " + endMarker;
        }

        return content;
    }

