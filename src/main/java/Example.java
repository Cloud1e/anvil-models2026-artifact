    public static void GenerateARepairPrompts(String[] providers) throws Exception {
        String modelsDir = "ARepair/experiments/models";
        String testSuiteDir = "ARepair/experiments/test-suite";
        
        File modelsFolder = new File(modelsDir);
        if (!modelsFolder.exists()) {
            System.err.println("ERROR: Models directory does not exist: " + modelsDir);
            return;
        }
        File[] modelFiles = modelsFolder.listFiles((dir, name) -> name.endsWith(".als"));
        if (modelFiles == null || modelFiles.length == 0) {
            System.err.println("ERROR: No model files found in " + modelsDir);
            return;
        }
        System.out.println("Found " + modelFiles.length + " model files");
        
        // Create directories for each provider
        for (String provider : providers) {
            String queryDir = "query/" + provider + "/ARepair";
            String resultDir = "result/" + provider + "/ARepair/responses";
            new File(queryDir).mkdirs();
            new File(resultDir).mkdirs();
        }
        
        int totalPrompts = 0;
        
        for (File modelFile : modelFiles) {
            String modelName = modelFile.getName().replace(".als", "");
            System.out.println("Processing model: " + modelName);
            String modelContent = new String(Files.readAllBytes(modelFile.toPath()));
            String[] lines = modelContent.split("\n");
            
            // Build the complete model, only removing body of predicates that need repair
            // Keep everything else: sig, fact, other predicates (like RepOk), etc.
            StringBuilder modelWithEmptyBodies = new StringBuilder();
            List<String> predicatesToRepair = new ArrayList<>();
            
            // First, identify which predicates need repair (skip RepOk and fact)
            for (int i = 0; i < lines.length; i++) {
                String line = lines[i].trim();
                if (line.startsWith("pred ") && !line.startsWith("pred RepOk")) {
                    String predName = line.substring(5).split("\\(")[0].trim();
                    if (predName.isEmpty() || predName.contains("{")) {
                        predName = line.substring(5).split("\\{")[0].trim();
                    }
                    predicatesToRepair.add(predName);
                    System.out.println("  Found predicate to repair: " + predName);
                }
            }
            
            // Build the model, keeping everything except the body of predicates to repair
            // Even if no predicates need repair, we still generate a prompt with the complete model
            int i = 0;
            while (i < lines.length) {
                String line = lines[i];
                String trimmedLine = line.trim();
                
                // Check if this is a predicate that needs repair
                boolean isPredicateToRepair = false;
                String currentPredName = null;
                if (trimmedLine.startsWith("pred ") && !trimmedLine.startsWith("pred RepOk")) {
                    currentPredName = trimmedLine.substring(5).split("\\(")[0].trim();
                    if (currentPredName.isEmpty() || currentPredName.contains("{")) {
                        currentPredName = trimmedLine.substring(5).split("\\{")[0].trim();
                    }
                    if (predicatesToRepair.contains(currentPredName)) {
                        isPredicateToRepair = true;
                    }
                }
                
                if (isPredicateToRepair) {
                    // Extract predicate with empty body
                    StringBuilder predContent = new StringBuilder();
                    int braceCount = 0;
                    boolean inPred = false;
                    int startLine = i;
                    
                    for (int j = i; j < lines.length; j++) {
                        String predLine = lines[j];
                        predContent.append(predLine).append("\n");
                        
                        for (char c : predLine.toCharArray()) {
                            if (c == '{') { braceCount++; inPred = true; }
                            if (c == '}') braceCount--;
                        }
                        
                        if (inPred && braceCount == 0) {
                            i = j + 1;
                            break;
                        }
                    }
                    
                    // Extract comment and create empty body (preserve comments inside body)
                    String predFull = predContent.toString();
                    String[] predLines = predFull.split("\n");
                    StringBuilder predWithEmptyBody = new StringBuilder();
                    boolean foundOpeningBrace = false;
                    boolean foundClosingBrace = false;
                    for (String predLine : predLines) {
                        String trimmed = predLine.trim();
                        if (trimmed.startsWith("pred ")) {
                            // Check if opening brace is on the same line
                            if (trimmed.contains("{")) {
                                // Extract predicate signature without the brace
                                String predSig = trimmed.substring(0, trimmed.indexOf("{")).trim();
                                predWithEmptyBody.append(predSig).append(" {\n");
                                foundOpeningBrace = true;
                            } else {
                                predWithEmptyBody.append(predLine).append("\n");
                            }
                        } else if (trimmed.startsWith("//") && !foundOpeningBrace) {
                            // Keep comments before the body
                            predWithEmptyBody.append(predLine).append("\n");
                        } else if (trimmed.equals("{") || (trimmed.startsWith("{") && !trimmed.startsWith("pred "))) {
                            predWithEmptyBody.append("{\n");
                            foundOpeningBrace = true;
                        } else if (foundOpeningBrace && !foundClosingBrace) {
                            // Keep comments inside the body, skip actual code
                            if (trimmed.startsWith("//")) {
                                // Preserve comments inside the body
                                predWithEmptyBody.append(predLine).append("\n");
                            } else if (trimmed.equals("}")) {
                                // Add placeholder before closing brace if no code was found
                                if (!predWithEmptyBody.toString().endsWith("  // your code go here\n")) {
                                    predWithEmptyBody.append("  // your code go here\n");
                                }
                                predWithEmptyBody.append("}\n");
                                foundClosingBrace = true;
                                break;
                            }
                            // Otherwise skip this line (it's body code, not comments)
                        } else if (trimmed.equals("}")) {
                            // Add placeholder before closing brace if no code was found
                            if (!predWithEmptyBody.toString().endsWith("  // your code go here\n")) {
                                predWithEmptyBody.append("  // your code go here\n");
                            }
                            predWithEmptyBody.append("}\n");
                            foundClosingBrace = true;
                            break;
                        }
                    }
                    // If we didn't find closing brace, add placeholder and closing brace
                    if (foundOpeningBrace && !foundClosingBrace) {
                        if (!predWithEmptyBody.toString().endsWith("  // your code go here\n")) {
                            predWithEmptyBody.append("  // your code go here\n");
                        }
                        predWithEmptyBody.append("}\n");
                    }
                    
                    modelWithEmptyBodies.append(predWithEmptyBody.toString());
                } else {
                    // Keep everything else as is (sig, fact, RepOk, etc.)
                    modelWithEmptyBodies.append(line).append("\n");
                    i++;
                }
            }
            
            String modelWithEmptyPredBodies = modelWithEmptyBodies.toString().trim();
            
            System.out.println("  Total predicates to repair: " + predicatesToRepair.size());
            
            // Skip models without predicates to repair (they don't need ARepair)
            if (predicatesToRepair.isEmpty()) {
                System.out.println("  Skipping " + modelName + " (no predicates to repair)");
                continue;
            }
            
            // Read test suite
            File testSuiteFile = new File(testSuiteDir, modelName + ".als");
            String testSuite = "";
            if (testSuiteFile.exists()) {
                testSuite = new String(Files.readAllBytes(testSuiteFile.toPath()));
            }
            
            // Build prompt containing the complete model with empty predicate bodies
            // Use test-driven synthesis: test-suite in prompt as constraints, LLM returns only predicate bodies
            StringBuilder prompt = new StringBuilder();
            // Has predicates to repair - use test-driven synthesis
            if (predicatesToRepair.size() == 1) {
                // Single predicate: ask for exactly 10 unique solutions
                String predName = predicatesToRepair.get(0);
            prompt.append("Give me exactly 10 unique solutions to the problem of synthesizing the body of the following Alloy predicate (without markdown or comments or ```) that are correct with respect to the properties described in the comments and pass all tests in the test suite:\n\n");
                prompt.append(modelWithEmptyPredBodies).append("\n\n");
                if (!testSuite.isEmpty()) {
                    prompt.append("// Test suite (use these tests to verify your solutions):\n");
                    prompt.append(testSuite).append("\n\n");
                }
            prompt.append("Each candidate must satisfy every run...expect command in the provided test suite. If a candidate fails any test, do not output it.\n");
            prompt.append("Return exactly 10 lines, each containing one predicate body only. No numbering, bullet points, explanations, or other text before/after these lines.\n");
            prompt.append("List each solution on a new line. Do not include the sig, fact, and pred declarations. Only return the predicate body (the expression inside the braces).\n");
            } else {
                // Multiple predicates: ask for solutions for each predicate
                prompt.append("Synthesize the following Alloy model by filling in the predicate bodies marked with '// your code go here'. The predicates should be correct with respect to the properties described in the comments and pass all tests in the test suite.\n");
            prompt.append("For each predicate that needs repair, provide exactly 10 unique solutions. Format: each line should be \"predicateName: body\", where body is the expression inside the braces.\n\n");
                prompt.append(modelWithEmptyPredBodies).append("\n\n");
                if (!testSuite.isEmpty()) {
                    prompt.append("// Test suite (use these tests to verify your solutions):\n");
                    prompt.append(testSuite).append("\n\n");
                }
            prompt.append("Each candidate must satisfy every run...expect command in the provided test suite. If a candidate fails any test, do not output it.\n");
            prompt.append("Return exactly 10 lines per predicate (total = 10 × number of predicates), each line formatted as \"predicateName: body\" with no numbering or commentary before/after the list. Do not include introductions, explanations, or any extra text outside these lines.\n");
            }
            
            // Save prompt file for each provider (one file per model)
            for (String provider : providers) {
                String queryDir = "query/" + provider + "/ARepair";
                String resultDir = "result/" + provider + "/ARepair/responses";
                String promptFile = queryDir + "/" + modelName;
                String responseFile = resultDir + "/" + modelName + ".txt";
                
                Files.write(Paths.get(promptFile), prompt.toString().getBytes(), 
                           StandardOpenOption.CREATE, StandardOpenOption.TRUNCATE_EXISTING);
                
                // Create empty response file if it doesn't exist
                if (!Files.exists(Paths.get(responseFile))) {
                    Files.write(Paths.get(responseFile), "".getBytes(), 
                               StandardOpenOption.CREATE);
                }
            }
            totalPrompts++;
        }
        
        System.out.println("\n=== Summary ===");
        System.out.println("Total models processed: " + totalPrompts);
        System.out.println("Providers: " + String.join(", ", providers));
        System.out.println("\nFiles created:");
        for (String provider : providers) {
            System.out.println("  query/" + provider + "/ARepair/ - " + totalPrompts + " prompt files (one per model)");
            System.out.println("  result/" + provider + "/ARepair/responses/ - " + totalPrompts + " empty response files (one per model)");
        }
    }
    
    /**
     * Process ARepair results from manually saved responses (Mercy/ARepair path: merge model + test suite).
     * Paths: {@code -Darepair.result.subfolder=<name>}.
     * For bounded {@code check { P <=> P2 }} re-evaluation without changing this method, call
     * {@link #ProcessARepairResultsEquivalence} (e.g. from {@code main} when {@code -Darepair.validation=equivalence}).
     */
    public static void ProcessARepairResults(FolderType folderType, boolean verbose) throws Exception {
        String resultDir = System.getProperty("arepair.result.dir", "").trim();
        if (resultDir.isEmpty()) {
            String baseFolder = "result" + File.separator + 
                (folderType == FolderType.DEEPSEEK ? "DeepSeek" : 
                 (folderType == FolderType.OPENAI ? "OpenAI" : 
                  (folderType == FolderType.CLAUDE ? "Claude" : "Gemini")));
            String subfolder = System.getProperty("arepair.result.subfolder", "ARepair");
            resultDir = baseFolder + File.separator + subfolder;
        }
        String responsesPath = resultDir + File.separator + "responses";
        String alloyDir = resultDir + File.separator + "Alloy";
        new File(alloyDir).mkdirs();
        
        String modelsDir = "ARepair/experiments/models";
        String testSuiteDir = "ARepair/experiments/test-suite";
        String subfolder = System.getProperty("arepair.result.subfolder", "ARepair");
        String queryDir = "query/" + folderType.name() + "/" + subfolder;
        
        Map<String, Map<String, Integer>> summaryTable = new TreeMap<>();
        
        File responsesFolder = new File(responsesPath);
        if (!responsesFolder.exists() || !responsesFolder.isDirectory()) {
            System.out.println("Responses directory does not exist: " + responsesPath);
            return;
        }
        File[] txtFiles = responsesFolder.listFiles((dir, name) -> name.endsWith(".txt"));
        if (txtFiles == null) return;
        
        String benchModelsProp = System.getProperty("arepair.bench.models", "").trim();
        Set<String> benchAllow = null;
        if (!benchModelsProp.isEmpty()) {
            benchAllow = new HashSet<>();
            for (String part : benchModelsProp.split(",")) {
                String t = part.trim();
                if (!t.isEmpty()) {
                    benchAllow.add(t);
                }
            }
            System.out.println("[arepair] bench filter (arepair.bench.models): " + benchAllow);
        }
        
        for (File txtFile : txtFiles) {
            String fileName = txtFile.getName();
            String modelName = fileName.substring(0, fileName.lastIndexOf('.'));
            if (benchAllow != null && !benchAllow.contains(modelName)) {
                continue;
            }
            
            // Read prompt file to identify which predicates need repair
            // The prompt file contains predicates with "// your code go here" markers
            File promptFile = new File(queryDir, modelName);
            String promptContent = "";
            if (promptFile.exists()) {
                promptContent = new String(Files.readAllBytes(promptFile.toPath()));
            }
            
            // Read model file for original structure
            File modelFile = new File(modelsDir, modelName + ".als");
            if (!modelFile.exists()) {
                System.out.println("Model file not found: " + modelName);
                continue;
            }
            
            String originalModelContent = new String(Files.readAllBytes(modelFile.toPath()));
            String[] lines = originalModelContent.split("\n");
            
            // Extract sig definitions and facts (everything before predicates)
            // Also extract predicates that don't need repair (to include in test model)
            StringBuilder sigAndFacts = new StringBuilder();
            List<String> predicatesToRepair = new ArrayList<>();
            Map<String, String> predSignatures = new HashMap<>(); // predName -> signature (e.g., "pred NoConflict()")
            Map<String, String> otherPredicates = new HashMap<>(); // predName -> full definition (for predicates not being repaired)
            Map<String, String> originalPredDefs = new HashMap<>(); // predName -> original predicate definition (raw, with whitespace)
            Map<String, String> predCommentBlocks = new HashMap<>(); // predName -> leading comment block inside predicate body
            Map<String, String> predBodyIndents = new HashMap<>(); // predName -> indentation for predicate body
            
            for (int i = 0; i < lines.length; i++) {
                String line = lines[i];
                String trimmedLine = line.trim();
                if (trimmedLine.startsWith("pred ") && !trimmedLine.startsWith("pred RepOk")) {
                    // Extract predicate name and signature
                    String predLine = line;
                    int bracePos = trimmedLine.indexOf("{");
                    if (bracePos == -1) {
                        // Multi-line signature
                        StringBuilder sigBuilder = new StringBuilder();
                        for (int j = i; j < lines.length; j++) {
                            sigBuilder.append(lines[j]).append("\n");
                            if (lines[j].trim().contains("{")) break;
                        }
                        predLine = sigBuilder.toString().trim();
                    }
                    // Extract predicate name: remove "pred " prefix, then extract name before any parameters or body
                    String predName = trimmedLine.substring(5).trim();
                    // Find first occurrence of [, (, or { to determine where name ends
                    int nameEnd = predName.length();
                    for (char delimiter : new char[]{'[', '(', '{'}) {
                        int pos = predName.indexOf(delimiter);
                        if (pos > 0 && pos < nameEnd) {
                            nameEnd = pos;
                        }
                    }
                    predName = predName.substring(0, nameEnd).trim();
                    
                    // Extract full predicate definition (including body)
                    StringBuilder fullPred = new StringBuilder();
                    int braceCount = 0;
                    boolean inPred = false;
                    int endIndex = i;
                    for (int j = i; j < lines.length; j++) {
                        fullPred.append(lines[j]).append("\n");
                        for (char c : lines[j].toCharArray()) {
                            if (c == '{') { braceCount++; inPred = true; }
                            if (c == '}') braceCount--;
                        }
                        if (inPred && braceCount == 0) {
                            endIndex = j;
                            break;
                        }
                    }
                    String fullPredDefRaw = fullPred.toString();
                    String fullPredDef = fullPredDefRaw.trim();
                    originalPredDefs.put(predName, fullPredDefRaw);
                    int braceOpen = fullPredDefRaw.indexOf("{");
                    int braceClose = fullPredDefRaw.lastIndexOf("}");
                    String bodyContent = "";
                    if (braceOpen >= 0 && braceClose > braceOpen) {
                        bodyContent = fullPredDefRaw.substring(braceOpen + 1, braceClose);
                    }
                    String commentBlock = extractLeadingCommentBlock(bodyContent);
                    String indentBlock = extractBodyIndent(bodyContent.substring(commentBlock.length()));
                    predCommentBlocks.put(predName, commentBlock);
                    predBodyIndents.put(predName, indentBlock);
                    
                    // Check if this predicate needs repair by checking the prompt file
                    // English-to-Alloy (ARepair/ARepairNoTest): predicate needs repair if prompt has "// your code go here"
                    // Alloy-to-Alloy (ARepair_Alloy2Alloy_*): prompt has full correct model (no placeholder) -> treat all non-RepOk preds as to repair
                    boolean needsRepair = false;
                    boolean isAlloy2Alloy = subfolder != null && subfolder.contains("Alloy2Alloy");
                    if (isAlloy2Alloy) {
                        needsRepair = true; // All predicates in model are to be rephrased (response lines are alternative bodies)
                    } else if (!promptContent.isEmpty()) {
                        String predNameInPrompt = "pred " + predName;
                        int predPos = promptContent.indexOf(predNameInPrompt);
                        if (predPos >= 0) {
                            String predSection = promptContent.substring(predPos, Math.min(predPos + 500, promptContent.length()));
                            needsRepair = predSection.contains("// your code go here");
                        }
                    } else {
                        needsRepair = fullPredDef.contains("// your code go here") || 
                                     fullPredDef.matches(".*\\{[\\s]*\\}.*");
                    }
                    
                    if (needsRepair) {
                        predicatesToRepair.add(predName);
                        predSignatures.put(predName, predLine);
                        // Also store the full definition for fallback (if response doesn't have this predicate)
                        otherPredicates.put(predName, fullPredDef);
                    } else {
                        // This predicate doesn't need repair, include it in test model
                        otherPredicates.put(predName, fullPredDef);
                    }
                    
                    i = endIndex;
                } else if (trimmedLine.startsWith("fun ")) {
                    // Include functions in sigAndFacts (they might be used by predicates)
                    StringBuilder fullFun = new StringBuilder();
                    int braceCount = 0;
                    boolean inFun = false;
                    int endIndex = i;
                    for (int j = i; j < lines.length; j++) {
                        fullFun.append(lines[j]).append("\n");
                        for (char c : lines[j].toCharArray()) {
                            if (c == '{') { braceCount++; inFun = true; }
                            if (c == '}') braceCount--;
                        }
                        if (inFun && braceCount == 0) {
                            endIndex = j;
                            break;
                        }
                    }
                    sigAndFacts.append(fullFun.toString());
                    i = endIndex;
                } else if (trimmedLine.startsWith("run ") || trimmedLine.startsWith("check ") || trimmedLine.startsWith("assert ")) {
                    // Skip run/check/assert commands in original model
                    break;
                } else {
                    sigAndFacts.append(line).append("\n");
                }
            }
            String sigAndFactsStr = sigAndFacts.toString().trim();
            
            // Read test suite
            File testSuiteFile = new File(testSuiteDir, modelName + ".als");
            String testSuite = "";
            if (testSuiteFile.exists()) {
                testSuite = new String(Files.readAllBytes(testSuiteFile.toPath()));
            }
            
            if (sigAndFactsStr.isEmpty()) {
                System.out.println("Could not extract sig and facts from model: " + modelName);
                continue;
            }
            
            String content = new String(Files.readAllBytes(txtFile.toPath())).trim();
            
            // For ARepair with test-driven synthesis, response file contains predicate bodies (one per line)
            // Each line is either a predicate body or "predName: body" format
            String[] responseLines = content.split("\\r?\\n");
            Set<String> uniqueResponses = new LinkedHashSet<>();
            for (String r : responseLines) {
                if (!r.trim().isEmpty()) {
                    uniqueResponses.add(r.trim());
                }
            }
            
            int total = uniqueResponses.size();
            int unique = uniqueResponses.size();
            int countCorrect = 0, countSyntax = 0, countWrong = 0;
            
            // Create detailed test report file
            String timestamp = java.time.LocalDateTime.now().format(java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd_HH-mm-ss"));
            Path detailedLogPath = Paths.get("log/Results", modelName + "_test_details_" + timestamp + ".log");
            Files.createDirectories(detailedLogPath.getParent());
            StringBuilder detailedLog = new StringBuilder();
            detailedLog.append(modelName.toUpperCase()).append(" Test Suite Detailed Report\n");
            detailedLog.append("Timestamp: ").append(timestamp).append("\n\n");
            detailedLog.append("Model run commands: ").append(originalModelContent.split("\\r?\\n").length > 0 ? 
                java.util.Arrays.stream(originalModelContent.split("\\r?\\n"))
                    .filter(l -> l.trim().startsWith("run ")).count() : 0).append("\n");
            
            StringBuilder alloyOutputContent = new StringBuilder();
            int candidateIdx = 1;
            for (String responseLine : uniqueResponses) {
                // Parse response line: extract predicate name and body
                // Format can be "predName: body" or just "body" (for single predicate)
                String predName = null;
                String predBody = null;
                
                // Check if line starts with a known predicate name followed by ":"
                boolean foundPredicateName = false;
                for (String knownPred : predicatesToRepair) {
                    if (responseLine.startsWith(knownPred + ":")) {
                        predName = knownPred;
                        predBody = responseLine.substring(knownPred.length() + 1).trim();
                        foundPredicateName = true;
                        break;
                    }
                }
                
                if (!foundPredicateName) {
                    // No predicate name prefix: assume it's for the first predicate (or only predicate)
                    if (predicatesToRepair.isEmpty()) {
                        System.out.println("Error: No predicates to repair for model " + modelName);
                        continue;
                    }
                    predName = predicatesToRepair.get(0);
                    predBody = responseLine;
                }
                
                if (!predSignatures.containsKey(predName)) {
                    System.out.println("Warning: Unknown predicate name in response: " + responseLine);
                    continue;
                }
                
                // Build test model: sig + facts + other predicates + pred (with response body) + test suite
                String predSig = predSignatures.get(predName);
                // Extract signature without body
                String predSignatureOnly = predSig;
                int bracePos = predSig.indexOf("{");
                if (bracePos > 0) {
                    predSignatureOnly = predSig.substring(0, bracePos).trim();
                }
                
                // Include predicates that don't need repair
                // Also include predicates that need repair but don't have a response in this line (use original body)
                StringBuilder otherPredsStr = new StringBuilder();
                for (Map.Entry<String, String> entry : otherPredicates.entrySet()) {
                    String otherPredName = entry.getKey();
                    String otherPredDef = entry.getValue();
                    // Only include if it's not the predicate we're repairing in this response
                    // (we'll add the repaired version separately)
                    if (!otherPredName.equals(predName)) {
                        otherPredsStr.append(otherPredDef).append("\n\n");
                    }
                }
                
                // Remove duplicate "open" statements from test suite
                // (original model already has them in sigAndFactsStr)
                String cleanedTestSuite = testSuite;
                if (sigAndFactsStr.contains("open util/")) {
                    // Remove "open util/..." lines from test suite to avoid duplicates
                    String[] testSuiteLines = testSuite.split("\\r?\\n");
                    StringBuilder cleaned = new StringBuilder();
                    for (String line : testSuiteLines) {
                        String trimmed = line.trim();
                        // Skip "open util/..." lines and "module ..." lines (already in sigAndFactsStr)
                        if (!trimmed.startsWith("open util/") && !trimmed.startsWith("module ")) {
                            cleaned.append(line).append("\n");
                        }
                    }
                    cleanedTestSuite = cleaned.toString();
                }
                
                String testModel = buildTestModelFromOriginal(originalModelContent, predName, predBody,
                        cleanedTestSuite, originalPredDefs, predCommentBlocks, predBodyIndents);
                if (testModel == null) {
                    testModel = sigAndFactsStr + "\n\n" +
                            otherPredsStr.toString() +
                            predSignatureOnly + " {\n" +
                            "  " + predBody + "\n" +
                            "}\n\n" +
                            cleanedTestSuite;
                }
                
                // Debug output for first response of each model
                boolean isFirstResponse = alloyOutputContent.length() == 0;
                if (isFirstResponse) {
                    System.out.println("\n=== Debug for " + modelName + " predicate " + predName + " ===");
                    System.out.println("Predicates to repair: " + predicatesToRepair);
                    System.out.println("Other predicates (not repaired): " + otherPredicates.keySet());
                    System.out.println("Response line: " + responseLine);
                    System.out.println("Extracted predName: " + predName);
                    System.out.println("Extracted predBody: " + predBody);
                    System.out.println("Predicate signature from model: " + predSig);
                    System.out.println("Predicate signature only: " + predSignatureOnly);
                    System.out.println("Other predicates string length: " + otherPredsStr.length());
                    
                    // Find where predicate appears in test model
                    int predPos = testModel.indexOf(predSignatureOnly);
                    if (predPos >= 0) {
                        System.out.println("\nPredicate section in test model (around position " + predPos + "):");
                        int start = Math.max(0, predPos - 100);
                        int end = Math.min(testModel.length(), predPos + 300);
                        System.out.println(testModel.substring(start, end));
                        if (end < testModel.length()) {
                            System.out.println("...");
                        }
                    } else {
                        System.out.println("WARNING: Predicate signature not found in test model!");
                        System.out.println("Test model (first 1000 chars):");
                        System.out.println(testModel.substring(0, Math.min(1000, testModel.length())));
                    }
                }
                
                // Write candidate info to detailed log
                detailedLog.append("==============================\n");
                detailedLog.append("Candidate ").append(candidateIdx).append("\n");
                detailedLog.append(responseLine).append("\n\n");
                
                CodeType resultType = AlloyProcess(testModel, isFirstResponse);
                String resultStr;
                if (resultType == CodeType.SYNTAX_ERROR) {
                        resultStr = "Syntax Error";
                        countSyntax++;
                        detailedLog.append("Result: Syntax Error\n\n");
                        Path synItemsDir = Paths.get(alloyDir, "SynItems");
                        try {
                            Files.createDirectories(synItemsDir);
                            String synId = modelName + "_" + predName + "_" + (candidateIdx - 1);
                            Path alsPath = synItemsDir.resolve(synId + ".als");
                            Path errPath = synItemsDir.resolve(synId + ".err");
                            Files.write(alsPath, testModel.getBytes(StandardCharsets.UTF_8));
                            String errMsg = lastSyntaxErrorMessage != null ? lastSyntaxErrorMessage : "";
                            Files.write(errPath, errMsg.getBytes(StandardCharsets.UTF_8));
                        } catch (IOException e) {
                            if (verbose) e.printStackTrace();
                        }
                    } else {
                        boolean allTestsPass = true;
                        int testsRun = 0;
                        Map<String, Integer> testExpectMap = new LinkedHashMap<>();
                        try {
                            CompModule world = CompUtil.parseEverything_fromString(new A4Reporter(), testModel);
                            A4Options opt = new A4Options();
                            List<edu.mit.csail.sdg.ast.Command> commands = world.getAllCommands();
                            String[] testSuiteLines = testSuite.split("\\r?\\n");
                            for (String line : testSuiteLines) {
                                line = line.trim();
                                if (line.startsWith("run ") && line.contains("expect ")) {
                                    int expectPos = line.indexOf("expect ");
                                    int forPos = line.indexOf(" for");
                                    if (expectPos > 0 && forPos > 0) {
                                        String testName = line.substring(4, forPos).trim();
                                        String expectStr = line.substring(expectPos + 7).trim();
                                        int expectedResult = expectStr.equals("1") ? 1 : 0;
                                        if (testName.equals("test26") && isFirstResponse) {
                                            System.out.println("DEBUG: Parsing test26: line='" + line + "', testName='" + testName + "', expectStr='" + expectStr + "', expectedResult=" + expectedResult);
                                        }
                                        Integer oldValue = testExpectMap.put(testName, expectedResult);
                                        if (testName.equals("test26") && oldValue != null && isFirstResponse) {
                                            System.out.println("WARNING: test26 was already in map with value " + oldValue + ", overwriting with " + expectedResult);
                                        }
                                    }
                                }
                            }
                            if (testExpectMap.isEmpty()) {
                                allTestsPass = true;
                            } else {
                                List<String> testNames = new ArrayList<>(testExpectMap.keySet());
                                int modelRunCommands = 0;
                                for (String line : originalModelContent.split("\\r?\\n")) {
                                    if (line.trim().startsWith("run ")) {
                                        modelRunCommands++;
                                    }
                                }
                                if (verbose) {
                                    System.out.println("Model has " + modelRunCommands + " run commands, test suite has " + testNames.size() + " tests");
                                    System.out.println("Total commands: " + commands.size());
                                }
                                Set<String> testsSeen = new HashSet<>();
                                for (edu.mit.csail.sdg.ast.Command cmd : commands) {
                                    String cmdLabel = (cmd.label == null) ? "" : cmd.label.trim();
                                    Integer expectedResult = testExpectMap.get(cmdLabel);
                                    if (expectedResult == null) {
                                        continue;
                                    }
                                    testsSeen.add(cmdLabel);
                                    if (testNames.indexOf(cmdLabel) == 0 && isFirstResponse && testsRun == 0) {
                                        System.out.println("Command for first test: label=" + cmdLabel + ", cmd=" + cmd.toString());
                                    }
                                    A4Solution solution = TranslateAlloyToKodkod.execute_command(
                                            null, world.getAllReachableSigs(), cmd, opt);
                                    int actualResult = solution.satisfiable() ? 1 : 0;
                                    testsRun++;
                                    String testStatus = (actualResult == expectedResult) ? "OK" : "FAIL";
                                    detailedLog.append(String.format("Test %-10s expect=%d actual=%d %s\n",
                                            cmdLabel, expectedResult, actualResult, testStatus));
                                    if (isFirstResponse && testsRun <= 5) {
                                        System.out.println("Test " + cmdLabel + ": expected=" + expectedResult + ", actual=" + actualResult);
                                    }
                                    if (actualResult != expectedResult) {
                                        allTestsPass = false;
                                        if (isFirstResponse) {
                                            System.out.println("Test failed: " + cmdLabel + " expected " + expectedResult + " but got " + actualResult);
                                        }
                                    }
                                }
                                if (testsSeen.size() < testExpectMap.size() && verbose) {
                                    System.out.println("Warning: Only ran " + testsSeen.size() + " tests out of " + testExpectMap.size());
                                }
                                if (testsRun == 0) {
                                    allTestsPass = false;
                                    if (verbose) {
                                        System.out.println("Warning: Found " + testExpectMap.size() + " tests but ran 0");
                                    }
                                } else if (testsRun < testExpectMap.size()) {
                                    if (verbose) {
                                        System.out.println("Warning: Ran " + testsRun + " tests out of " + testExpectMap.size());
                                    }
                                }
                            }
                        } catch (Exception e) {
                            allTestsPass = false;
                            if (verbose) {
                                System.out.println("Exception during test execution: " + e.getMessage());
                                e.printStackTrace();
                            }
                        }
                        if (allTestsPass) {
                            resultStr = "Correct";
                            countCorrect++;
                        } else {
                            resultStr = "Wrong";
                            countWrong++;
                        }
                        detailedLog.append("\nResult: ").append(resultStr);
                        detailedLog.append(" (Tests run: ").append(testsRun);
                        if (testExpectMap.size() > 0) {
                            detailedLog.append("/").append(testExpectMap.size());
                        }
                        detailedLog.append(")\n\n");
                }
                alloyOutputContent.append(responseLine).append("\n").append(resultStr);
                if (resultType == CodeType.SYNTAX_ERROR && lastSyntaxErrorMessage != null && !lastSyntaxErrorMessage.isEmpty()) {
                    alloyOutputContent.append("\nSYN_MSG:\n").append(lastSyntaxErrorMessage).append("\nEND_SYN_MSG");
                }
                alloyOutputContent.append("\n\n");
                candidateIdx++;
            }
            
            // Write detailed log to file
            Files.write(detailedLogPath, detailedLog.toString().getBytes());
            System.out.println("Detailed test report written to: " + detailedLogPath);
            
            alloyOutputContent.append("Total: ").append(total).append("\n")
                    .append("Unique: ").append(unique).append("\n")
                    .append("Correct: ").append(countCorrect).append("\n")
                    .append("Syntax Error: ").append(countSyntax).append("\n")
                    .append("Wrong: ").append(countWrong).append("\n");
            
            Path alloyFilePath = Paths.get(alloyDir, fileName);
            Files.write(alloyFilePath, alloyOutputContent.toString().getBytes());
            
            Map<String, Integer> stats = new HashMap<>();
            stats.put("Unique", unique);
            stats.put("Correct", countCorrect);
            stats.put("Syntax Error", countSyntax);
            stats.put("Wrong", countWrong);
            summaryTable.put(modelName, stats);
        }
        
        // Generate LaTeX table (reuse existing logic)
        StringBuilder table = new StringBuilder();
        table.append("\\begin{table}\n")
                .append("\\centering\n")
                .append("\\caption{ARepair Results}\\label{tab_arepair}\n")
                .append("\\begin{tabular}{|l|l|l|l|l|}\n")
                .append("\\hline\n")
                .append("Model & Unique & Cor & Syn & Sem \\\\\n")
                .append("\\hline\n");
        for (String model : summaryTable.keySet()) {
            Map<String, Integer> stats = summaryTable.get(model);
            table.append(model).append(" & ")
                    .append(stats.get("Unique")).append(" & ")
                    .append(stats.get("Correct")).append(" & ")
                    .append(stats.get("Syntax Error")).append(" & ")
                    .append(stats.get("Wrong")).append(" \\\\\n")
                    .append("\\hline\n");
        }
        table.append("\\end{tabular}\n")
                .append("\\end{table}\n");
        System.out.println("\n" + table);
    }

    /**
     * Same inputs as {@link #ProcessARepairResults}, but validates each candidate with bounded
     * {@code check { gold <=> cand }} via {@link #AlloyProcess} (no test-suite file).
     * Scope: {@code -Darepair.equiv.scope=N} (default 5).
     */
    public static void ProcessARepairResultsEquivalence(FolderType folderType, boolean verbose) throws Exception {
        String resultDir = System.getProperty("arepair.result.dir", "").trim();
        if (resultDir.isEmpty()) {
            String baseFolder = "result" + File.separator +
                (folderType == FolderType.DEEPSEEK ? "DeepSeek" :
                 (folderType == FolderType.OPENAI ? "OpenAI" :
                  (folderType == FolderType.CLAUDE ? "Claude" : "Gemini")));
            String subfolder = System.getProperty("arepair.result.subfolder", "ARepair");
            resultDir = baseFolder + File.separator + subfolder;
        }
        String responsesPath = resultDir + File.separator + "responses";
        String alloyDir = resultDir + File.separator + "Alloy";
        new File(alloyDir).mkdirs();

        String modelsDir = "ARepair/experiments/models";
        String subfolder = System.getProperty("arepair.result.subfolder", "ARepair");
        String queryDir = "query/" + folderType.name() + "/" + subfolder;

        Map<String, Map<String, Integer>> summaryTable = new TreeMap<>();

        File responsesFolder = new File(responsesPath);
        if (!responsesFolder.exists() || !responsesFolder.isDirectory()) {
            System.out.println("Responses directory does not exist: " + responsesPath);
            return;
        }
        File[] txtFiles = responsesFolder.listFiles((dir, name) -> name.endsWith(".txt"));
        if (txtFiles == null) return;

        String benchModelsProp = System.getProperty("arepair.bench.models", "").trim();
        Set<String> benchAllow = null;
        if (!benchModelsProp.isEmpty()) {
            benchAllow = new HashSet<>();
            for (String part : benchModelsProp.split(",")) {
                String t = part.trim();
                if (!t.isEmpty()) {
                    benchAllow.add(t);
                }
            }
            System.out.println("[arepair] bench filter (equiv) (arepair.bench.models): " + benchAllow);
        }

        for (File txtFile : txtFiles) {
            String fileName = txtFile.getName();
            String modelName = fileName.substring(0, fileName.lastIndexOf('.'));
            if (benchAllow != null && !benchAllow.contains(modelName)) {
                continue;
            }

            File promptFile = new File(queryDir, modelName);
            String promptContent = "";
            if (promptFile.exists()) {
                promptContent = new String(Files.readAllBytes(promptFile.toPath()));
            }

            File modelFile = new File(modelsDir, modelName + ".als");
            if (!modelFile.exists()) {
                System.out.println("Model file not found: " + modelName);
                continue;
            }

            String originalModelContent = new String(Files.readAllBytes(modelFile.toPath()));
            String[] lines = originalModelContent.split("\n");

            StringBuilder sigAndFacts = new StringBuilder();
            List<String> predicatesToRepair = new ArrayList<>();
            Map<String, String> predSignatures = new HashMap<>();
            Map<String, String> otherPredicates = new HashMap<>();
            Map<String, String> originalPredDefs = new HashMap<>();
            Map<String, String> predCommentBlocks = new HashMap<>();
            Map<String, String> predBodyIndents = new HashMap<>();

            for (int i = 0; i < lines.length; i++) {
                String line = lines[i];
                String trimmedLine = line.trim();
                if (trimmedLine.startsWith("pred ") && !trimmedLine.startsWith("pred RepOk")) {
                    String predLine = line;
                    int bracePos = trimmedLine.indexOf("{");
                    if (bracePos == -1) {
                        StringBuilder sigBuilder = new StringBuilder();
                        for (int j = i; j < lines.length; j++) {
                            sigBuilder.append(lines[j]).append("\n");
                            if (lines[j].trim().contains("{")) break;
                        }
                        predLine = sigBuilder.toString().trim();
                    }
                    String predName = trimmedLine.substring(5).trim();
                    int nameEnd = predName.length();
                    for (char delimiter : new char[]{'[', '(', '{'}) {
                        int pos = predName.indexOf(delimiter);
                        if (pos > 0 && pos < nameEnd) {
                            nameEnd = pos;
                        }
                    }
                    predName = predName.substring(0, nameEnd).trim();

                    StringBuilder fullPred = new StringBuilder();
                    int braceCount = 0;
                    boolean inPred = false;
                    int endIndex = i;
                    for (int j = i; j < lines.length; j++) {
                        fullPred.append(lines[j]).append("\n");
                        for (char c : lines[j].toCharArray()) {
                            if (c == '{') { braceCount++; inPred = true; }
                            if (c == '}') braceCount--;
                        }
                        if (inPred && braceCount == 0) {
                            endIndex = j;
                            break;
                        }
                    }
                    String fullPredDefRaw = fullPred.toString();
                    String fullPredDef = fullPredDefRaw.trim();
                    originalPredDefs.put(predName, fullPredDefRaw);
                    int braceOpen = fullPredDefRaw.indexOf("{");
                    int braceClose = fullPredDefRaw.lastIndexOf("}");
                    String bodyContent = "";
                    if (braceOpen >= 0 && braceClose > braceOpen) {
                        bodyContent = fullPredDefRaw.substring(braceOpen + 1, braceClose);
                    }
                    String commentBlock = extractLeadingCommentBlock(bodyContent);
                    String indentBlock = extractBodyIndent(bodyContent.substring(commentBlock.length()));
                    predCommentBlocks.put(predName, commentBlock);
                    predBodyIndents.put(predName, indentBlock);

                    boolean needsRepair = false;
                    boolean isAlloy2Alloy = subfolder != null && subfolder.contains("Alloy2Alloy");
                    if (isAlloy2Alloy) {
                        needsRepair = true;
                    } else if (!promptContent.isEmpty()) {
                        String predNameInPrompt = "pred " + predName;
                        int predPos = promptContent.indexOf(predNameInPrompt);
                        if (predPos >= 0) {
                            String predSection = promptContent.substring(predPos, Math.min(predPos + 500, promptContent.length()));
                            needsRepair = predSection.contains("// your code go here");
                        }
                    } else {
                        needsRepair = fullPredDef.contains("// your code go here") ||
                                     fullPredDef.matches(".*\\{[\\s]*\\}.*");
                    }

                    if (needsRepair) {
                        predicatesToRepair.add(predName);
                        predSignatures.put(predName, predLine);
                        otherPredicates.put(predName, fullPredDef);
                    } else {
                        otherPredicates.put(predName, fullPredDef);
                    }

                    i = endIndex;
                } else if (trimmedLine.startsWith("fun ")) {
                    StringBuilder fullFun = new StringBuilder();
                    int braceCount = 0;
                    boolean inFun = false;
                    int endIndex = i;
                    for (int j = i; j < lines.length; j++) {
                        fullFun.append(lines[j]).append("\n");
                        for (char c : lines[j].toCharArray()) {
                            if (c == '{') { braceCount++; inFun = true; }
                            if (c == '}') braceCount--;
                        }
                        if (inFun && braceCount == 0) {
                            endIndex = j;
                            break;
                        }
                    }
                    sigAndFacts.append(fullFun.toString());
                    i = endIndex;
                } else if (trimmedLine.startsWith("run ") || trimmedLine.startsWith("check ") || trimmedLine.startsWith("assert ")) {
                    break;
                } else {
                    sigAndFacts.append(line).append("\n");
                }
            }
            String sigAndFactsStr = sigAndFacts.toString().trim();

            if (sigAndFactsStr.isEmpty()) {
                System.out.println("Could not extract sig and facts from model: " + modelName);
                continue;
            }

            String content = new String(Files.readAllBytes(txtFile.toPath())).trim();
            String[] responseLines = content.split("\\r?\\n");
            Set<String> uniqueResponses = new LinkedHashSet<>();
            for (String r : responseLines) {
                if (!r.trim().isEmpty()) {
                    uniqueResponses.add(r.trim());
                }
            }

            int total = uniqueResponses.size();
            int unique = uniqueResponses.size();
            int countCorrect = 0, countSyntax = 0, countWrong = 0;

            String timestamp = java.time.LocalDateTime.now().format(java.time.format.DateTimeFormatter.ofPattern("yyyy-MM-dd_HH-mm-ss"));
            Path detailedLogPath = Paths.get("log/Results", modelName + "_equiv_details_" + timestamp + ".log");
            Files.createDirectories(detailedLogPath.getParent());
            StringBuilder detailedLog = new StringBuilder();
            detailedLog.append(modelName.toUpperCase()).append(" Bounded equivalence report\n");
            detailedLog.append("Timestamp: ").append(timestamp).append("\n");
            int equivScopeLog = 5;
            try {
                equivScopeLog = Integer.parseInt(System.getProperty("arepair.equiv.scope", "5"));
            } catch (NumberFormatException ignored) { }
            detailedLog.append("Scope: for ").append(equivScopeLog).append(" (-Darepair.equiv.scope)\n\n");

            StringBuilder alloyOutputContent = new StringBuilder();
            int candidateIdx = 1;
            for (String responseLine : uniqueResponses) {
                String predName = null;
                String predBody = null;
                boolean foundPredicateName = false;
                for (String knownPred : predicatesToRepair) {
                    if (responseLine.startsWith(knownPred + ":")) {
                        predName = knownPred;
                        predBody = responseLine.substring(knownPred.length() + 1).trim();
                        foundPredicateName = true;
                        break;
                    }
                }

                if (!foundPredicateName) {
                    if (predicatesToRepair.isEmpty()) {
                        System.out.println("Error: No predicates to repair for model " + modelName);
                        continue;
                    }
                    predName = predicatesToRepair.get(0);
                    predBody = responseLine;
                }

                if (!predSignatures.containsKey(predName)) {
                    System.out.println("Warning: Unknown predicate name in response: " + responseLine);
                    continue;
                }

                boolean isFirstResponse = alloyOutputContent.length() == 0;
                if (isFirstResponse && verbose) {
                    System.out.println("\n=== Equiv " + modelName + " / " + predName + " ===");
                }

                detailedLog.append("==============================\n");
                detailedLog.append("Candidate ").append(candidateIdx).append("\n");
                detailedLog.append(responseLine).append("\n\n");

                String equivModel = null;
                try {
                    equivModel = buildArepairEquivalenceModule(sigAndFactsStr, otherPredicates, predName, predBody, originalPredDefs);
                } catch (IllegalArgumentException ex) {
                    if (verbose) ex.printStackTrace();
                    detailedLog.append("Equiv build error: ").append(ex.getMessage()).append("\n");
                }

                CodeType resultType = CodeType.SYNTAX_ERROR;
                String resultStr;
                if (equivModel == null) {
                    lastSyntaxErrorMessage = null;
                    resultStr = "Syntax Error";
                    countSyntax++;
                    detailedLog.append("Result: Syntax Error (equiv module missing or build failed)\n\n");
                } else {
                    lastSyntaxErrorMessage = null;
                    resultType = AlloyProcess(equivModel, isFirstResponse && verbose);
                    if (resultType == CodeType.SYNTAX_ERROR) {
                        resultStr = "Syntax Error";
                        countSyntax++;
                        detailedLog.append("Result: Syntax Error\n\n");
                        Path synItemsDir = Paths.get(alloyDir, "SynItems");
                        try {
                            Files.createDirectories(synItemsDir);
                            String synId = modelName + "_" + predName + "_equiv_" + (candidateIdx - 1);
                            Path alsPath = synItemsDir.resolve(synId + ".als");
                            Path errPath = synItemsDir.resolve(synId + ".err");
                            Files.write(alsPath, equivModel.getBytes(StandardCharsets.UTF_8));
                            String errMsg = lastSyntaxErrorMessage != null ? lastSyntaxErrorMessage : "";
                            Files.write(errPath, errMsg.getBytes(StandardCharsets.UTF_8));
                        } catch (IOException e) {
                            if (verbose) e.printStackTrace();
                        }
                    } else if (resultType == CodeType.DIFF_OUTPUT) {
                        resultStr = "Wrong";
                        countWrong++;
                        detailedLog.append("Result: Wrong (counterexample — not equivalent at this scope)\n\n");
                    } else {
                        resultStr = "Correct";
                        countCorrect++;
                        detailedLog.append("Result: Correct (no counterexample — equivalent at this scope)\n\n");
                    }
                }

                alloyOutputContent.append(responseLine).append("\n").append(resultStr);
                if (resultType == CodeType.SYNTAX_ERROR && lastSyntaxErrorMessage != null && !lastSyntaxErrorMessage.isEmpty()) {
                    alloyOutputContent.append("\nSYN_MSG:\n").append(lastSyntaxErrorMessage).append("\nEND_SYN_MSG");
                }
                alloyOutputContent.append("\n\n");
                candidateIdx++;
            }

            Files.write(detailedLogPath, detailedLog.toString().getBytes());
            System.out.println("Detailed equivalence report written to: " + detailedLogPath);

            alloyOutputContent.append("Total: ").append(total).append("\n")
                    .append("Unique: ").append(unique).append("\n")
                    .append("Correct: ").append(countCorrect).append("\n")
                    .append("Syntax Error: ").append(countSyntax).append("\n")
                    .append("Wrong: ").append(countWrong).append("\n");

            Path alloyFilePath = Paths.get(alloyDir, fileName);
            Files.write(alloyFilePath, alloyOutputContent.toString().getBytes());

            Map<String, Integer> stats = new HashMap<>();
            stats.put("Unique", unique);
            stats.put("Correct", countCorrect);
            stats.put("Syntax Error", countSyntax);
            stats.put("Wrong", countWrong);
            summaryTable.put(modelName, stats);
        }

        StringBuilder table = new StringBuilder();
        table.append("\\begin{table}\n")
                .append("\\centering\n")
                .append("\\caption{ARepair Results (bounded equivalence)}\\label{tab_arepair_equiv}\n")
                .append("\\begin{tabular}{|l|l|l|l|l|}\n")
                .append("\\hline\n")
                .append("Model & Unique & Cor & Syn & Sem \\\\\n")
                .append("\\hline\n");
        for (String model : summaryTable.keySet()) {
            Map<String, Integer> stats = summaryTable.get(model);
            table.append(model).append(" & ")
                    .append(stats.get("Unique")).append(" & ")
                    .append(stats.get("Correct")).append(" & ")
                    .append(stats.get("Syntax Error")).append(" & ")
                    .append(stats.get("Wrong")).append(" \\\\\n")
                    .append("\\hline\n");
        }
        table.append("\\end{tabular}\n")
                .append("\\end{table}\n");
        System.out.println("\n" + table);
    }

