    private static String extractLeadingCommentBlock(String bodyContent) {
        StringBuilder sb = new StringBuilder();
        int idx = 0;
        int len = bodyContent.length();
        while (idx < len) {
            int lineEnd = bodyContent.indexOf('\n', idx);
            if (lineEnd == -1) lineEnd = len;
            String line = bodyContent.substring(idx, lineEnd);
            String trimmed = line.trim();
            if (trimmed.isEmpty() || trimmed.startsWith("//") || trimmed.startsWith("/*")) {
                sb.append(line);
                if (lineEnd < len) sb.append("\n");
                idx = lineEnd + 1;
            } else {
                break;
            }
        }
        return sb.toString();
    }

    private static String extractBodyIndent(String remaining) {
        String[] lines = remaining.split("\\n");
        for (String line : lines) {
            String trimmed = line.trim();
            if (trimmed.isEmpty()) continue;
            int idx = line.indexOf(trimmed);
            if (idx > 0) {
                return line.substring(0, idx);
            } else {
                return "";
            }
        }
        return "  ";
    }

    private static String buildTestModelFromOriginal(
            String originalModel,
            String predName,
            String predBody,
            String testSuiteContent,
            Map<String, String> originalPredDefs,
            Map<String, String> predCommentBlocks,
            Map<String, String> predBodyIndents) {

        String originalDef = originalPredDefs.get(predName);
        if (originalDef == null) return null;

        int braceOpen = originalDef.indexOf("{");
        int braceClose = originalDef.lastIndexOf("}");
        if (braceOpen < 0 || braceClose <= braceOpen) return null;

        String header = originalDef.substring(0, braceOpen + 1);
        String footer = originalDef.substring(braceClose);
        String commentBlock = predCommentBlocks.getOrDefault(predName, "");
        String indent = predBodyIndents.getOrDefault(predName, "  ");

        StringBuilder newDef = new StringBuilder();
        newDef.append(header);
        if (!header.endsWith("\n")) newDef.append("\n");
        if (!commentBlock.isEmpty()) {
            newDef.append(commentBlock);
            if (!commentBlock.endsWith("\n")) newDef.append("\n");
        }
        newDef.append(indent).append(predBody).append("\n");
        if (!footer.startsWith("\n")) newDef.append("\n");
        newDef.append(footer);

        String replacedModel = originalModel.replaceFirst(
                Pattern.quote(originalDef),
                Matcher.quoteReplacement(newDef.toString()));
        if (replacedModel.equals(originalModel)) return null;
        return replacedModel + "\n\n" + testSuiteContent;
    }
